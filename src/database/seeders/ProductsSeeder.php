                $this->hotizontalMillingMachines(),
                $this->wideUniversalMillingMachines(),
                $this->ringBendingMachines(),
                $this->latheMachines(),
                $this->flatbedPlotter(),
                $this->pipeFinningMachines()
            )
        );

        // Теперь обновляем случайным образом is_sale и создаем изображения
        $products = Product::all();
        foreach ($products as $product) {
            // Устанавливаем случайную скидку
            $product->update([
                'is_sale' => rand(1, 100) <= 30 // 30% вероятность скидки
            ]);

            // Создаем основное изображение
            ProductImage::create([
                'product_id' => $product->id,
                'name' => $product->slug,
                'sort_order' => 1,
                'is_primary' => true
            ]);
        }
    }

